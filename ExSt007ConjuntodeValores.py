import math
Num = 0

while Num <= 0:
    Num = float(input("Digite um Número (Positivo e acima de zero)"))


print("O do número digitado é: ", math.pow(Num, 2), "O cubo: ", math.pow(Num, 3),
      "E a raiz quadrada:", math.sqrt(Num))


